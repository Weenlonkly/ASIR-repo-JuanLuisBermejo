# Declara una variable con tu edad. Luego usa esta variable para calcular cuántos días, horas y minutos has vivido (aproximadamente).
#    Ejemplo:
#    Días: edad * 365
#    Horas: días * 24
#    Minutos: horas * 60


edad = 21

dias = edad * 365
horas = dias * 24
minutos = horas * 60
segundos = minutos * 60

print(f"Has vivido aproximadamente {dias} días, que son {horas} horas, que son {minutos} minutos y que son {segundos} segundos.")